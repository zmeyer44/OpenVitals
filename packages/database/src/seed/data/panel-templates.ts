interface PanelMetricSeed {
  panelId: string;
  metricCode: string;
  isCore: boolean;
  sortOrder: number;
}

function buildMetrics(panelId: string, codes: string[]): PanelMetricSeed[] {
  return codes.map((code, i) => ({
    panelId,
    metricCode: code,
    isCore: true,
    sortOrder: i + 1,
  }));
}

